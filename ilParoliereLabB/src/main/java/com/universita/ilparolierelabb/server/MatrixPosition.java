public class MatrixPosition {
    private int x=0; 
    private int y=0;
    
    public MatrixPosition(){}
    
    public int getX(){
        return this.x; 
    }
    
    public void setX(int x){
        this.x=x; 
    }
    
    public int getY(){
        return this.y;
    }
    
    public void setY(int y){
        this.y=y; 
    }
}
